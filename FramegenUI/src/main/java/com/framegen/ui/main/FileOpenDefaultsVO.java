package com.framegen.ui.main;

import java.io.File;

public class FileOpenDefaultsVO {

	private File baseImageOpenDir;
	private File overlayImageOpenDir;
	private File outputOpenDir;
	
	public File getBaseImageOpenDir() {
		return baseImageOpenDir;
	}
	public void setBaseImageOpenDir(File baseImageOpenDir) {
		this.baseImageOpenDir = baseImageOpenDir;
	}
	public File getOverlayImageOpenDir() {
		return overlayImageOpenDir;
	}
	public void setOverlayImageOpenDir(File overlayImageOpenDir) {
		this.overlayImageOpenDir = overlayImageOpenDir;
	}
	public File getOutputOpenDir() {
		return outputOpenDir;
	}
	public void setOutputOpenDir(File outputOpenDir) {
		this.outputOpenDir = outputOpenDir;
	}
	
	
}
